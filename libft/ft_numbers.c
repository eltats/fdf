int		ft_numbers(int nb)
{
	if (nb == -2147483648)
		return (1 + ft_numbers(-147483648));
	if (nb < 0)
		return (1 + ft_numbers(nb * -1));
	if (nb >= 10)
		return (1 + ft_numbers(nb / 10));
	else
		return (1);
}
